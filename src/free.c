#include "cub3D.h"

void	ft_free_int_tab(int **tab)
{
	int i;

	i = 0;
	while (tab[i][0] != -1 && tab[i][1] != -1)
		free(tab[i++]);
	free(tab[i]);
	free(tab);
}
