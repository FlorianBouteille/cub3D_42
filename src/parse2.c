#include "cub3D.h"

char	check_line(char *line)
{
	char	c;

	c = 0;
	if (line[0] == 'N' && (line[1] == ' ' || (line[1] == 'O'
				&& line[2] == ' ')))
		c = 'N';
	if (line[0] == 'S' && (line[1] == ' ' || (line[1] == 'O'
				&& line[2] == ' ')))
		c = 'S';
	if (line[0] == 'W' && (line[1] == ' ' || (line[1] == 'E'
				&& line[2] == ' ')))
		c = 'W';
	if (line[0] == 'E' && (line[1] == ' ' || (line[1] == 'A'
				&& line[2] == ' ')))
		c = 'E';
	if (line[0] == 'F' && line[1] == ' ')
		c = 'F';
	if (line[0] == 'C' && line[1] == ' ')
		c = 'C';
	return (c);
}
